pub fn map_key_code(c: &str) -> u16 {
	match c {
		"18" => 0x02, // 1
		"19" => 0x03, // 2
		"20" => 0x04, // 3
		"21" => 0x05, // 4
		"23" => 0x06, // 5
		"22" => 0x07, // 6
		"26" => 0x08, // 7
		"28" => 0x09, // 8
		"25" => 0x0A, // 9
		"29" => 0x0B, // 0
		"27" => 0x0C, // -
		// ";+" => 0x0D, // =
		"48" => 0x0F, // tab
		"12" => 0x10, // q
		"13" => 0x11, // w
		"14" => 0x12, // e
		"15" => 0x13, // r
		"17" => 0x14, // t
		"16" => 0x15, // y
		"32" => 0x16, // u
		"34" => 0x17, // i
		"31" => 0x18, // o
		"35" => 0x19, // p
		"33" => 0x1A, // [
		"30" => 0x1B, // ]
		"0" => 0x1E,  // a
		"1" => 0x1F,  // s
		"2" => 0x20,  // d
		"3" => 0x21,  // f
		"5" => 0x22,  // g
		"4" => 0x23,  // h
		"38" => 0x24, // j
		"40" => 0x25, // k
		"37" => 0x26, // l
		"41" => 0x27, // ;
		// "39" => 0x28, // '
		// "\\" => 0x2B, // \
		"6" => 0x2C,  // z
		"7" => 0x2D,  // x
		"8" => 0x2E,  // c
		"9" => 0x2F,  // v
		"11" => 0x30, // b
		"45" => 0x31, // n
		"46" => 0x32, // m
		"43" => 0x33, // ,
		"47" => 0x34, // .
		"44" => 0x35, // /

		// F-keys
		"122" => 0x3b, // F1
		"120" => 0x3c, // F2
		"99" => 0x3d,  // F3
		"118" => 0x3e, // F4
		"96" => 0x3f,  // F5
		"97" => 0x40,  // F6
		"98" => 0x41,  // F7
		"100" => 0x42, // F8
		"101" => 0x43, // F9
		"109" => 0x44, // F10
		"103" => 0x57, // F11
		"111" => 0x58, // F12

		// Misc keys
		"53" => 0x01,        // ESC
		"36" => 0x1c,        // return
		"49" => 0x39,        // space
		"51" => 0x0e,        // back space
		"58" => 0xE038,      // alt
		"55" | "59" => 0x1D, // ctrl or cmd
		"56" => 0x2A,
		// Right group
		"123" => 0xE04B, // left arrow
		"124" => 0xE04D, // right arrow
		"125" => 0xE050, // down arrow
		"126" => 0xE048, // up arrow

		// special
		"102" | "104" => 0x29,
		// 半角
		_ => 0,
	}
}
